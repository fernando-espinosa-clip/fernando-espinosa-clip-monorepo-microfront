export const generateOrders = (number = 100) => {
  const orderNumbers = createArray(number);
  let baseDate = new Date();
  const baseOrders = {
    id: 'f69f88012978187a6c12897f',
    ref: 'DEV1049',
    amount: 30.5,
    customer: {},
    products: [],
    createdAt: '2023-08-08T21:09:03.333Z',
    status: 'pending',
  };
  const newOrders = [];
  for (let x = 0; x < orderNumbers.length; x++) {
    // unshift
    const order = { ...baseOrders };
    let orderProducts = createArray(getRandomArbitrary(1, 5));
    const customer = customers1[getRandomArbitrary(0, customers1.length - 1)];
    let amount = 0;
    const productsIds = [];
    orderProducts = orderProducts.map(() => {
      const product = products[getRandomArbitrary(0, products.length - 1)];
      amount += product.price;
      productsIds.push(product.id);
    });
    let orderStatus = 'error';
    const statusProbability = getRandomArbitrary(1, 100);
    if (statusProbability >= 50) {
      orderStatus = 'success';
    }
    if (statusProbability <= 49 && statusProbability >= 15) {
      orderStatus = 'warning';
    }
    order.id = uuidv4();
    order.ref = `DEV${1000 + x}`;
    order.amount = amount;
    order.products = productsIds;
    order.customer = customer.id;
    order.createdAt = baseDate;
    order.status = orderStatus;
    baseDate = subMinutes(baseDate, getRandomArbitrary(1, 45));
    newOrders.unshift(order);
  }
  return newOrders;
};
